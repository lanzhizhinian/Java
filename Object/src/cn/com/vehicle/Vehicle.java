package cn.com.vehicle;

public class Vehicle {
	private String brand;
	private float weight;
	
	public Vehicle() {
		;
	}

	public String getBrand() {
		return brand;
	}

	public void setBrand(String brand) {
		this.brand = brand;
	}

	public float getWeight() {
		return weight;
	}

	public void setWeight(float weight) {
		this.weight = weight;
	}
	
}
